#include "ft_printf.h"

int	nbrlen(int x)
{
	int	len;

	len = 0;
	if (x <= 9 && x >= 0)
		return (1);
	else if (x < 0)
		len = 1;
	while (x != 0)
	{
		x = x / 10;
		len++;
	}
	return (len);
}
